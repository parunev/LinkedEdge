package com.parunev.linkededge.model.enums;

/**
 * The `QuestionDifficulty` enum represents different difficulty levels for questions in the LinkedEdge application. These levels are used to categorize
 * and generate interview questions of varying complexity.
 *
 * @author Martin Parunev
 * @date October 11, 2023
 */
public enum QuestionDifficulty {
    /**
     * Easy difficulty level.
     */
    EASY,

    /**
     * Moderate difficulty level.
     */
    MODERATE,

    /**
     * Hard difficulty level.
     */
    HARD,

    /**
     * Expert difficulty level.
     */
    EXPERT
}
